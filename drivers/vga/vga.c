/* ── Framebuffer ──────────────────────────────────────────────────────────── */
#define VGA_FB  ((volatile u8 *)0xA0000)

/* ── VGA port addresses ───────────────────────────────────────────────────── */
#define VGA_MISC_WRITE   0x3C2
#define VGA_SEQ_INDEX    0x3C4
#define VGA_SEQ_DATA     0x3C5
#define VGA_DAC_WRITE    0x3C8
#define VGA_DAC_DATA     0x3C9
#define VGA_GC_INDEX     0x3CE
#define VGA_GC_DATA      0x3CF
#define VGA_CRTC_INDEX   0x3D4
#define VGA_CRTC_DATA    0x3D5
#define VGA_AC_INDEX     0x3C0
#define VGA_INSTAT_READ  0x3DA   /* reading resets AC flip-flop */

/* ── Mode 13h register tables ────────────────────────────────────────────── */

/* Miscellaneous Output Register */
static const u8 misc_reg = 0x63;

/* Sequencer: indices 0–4 */
static const u8 seq_regs[] = { 0x03, 0x01, 0x0F, 0x00, 0x0E };

/* CRT Controller: indices 0–24 */
static const u8 crtc_regs[] = {
    0x5F, 0x4F, 0x50, 0x82, 0x54, 0x80, 0xBF, 0x1F,
    0x00, 0x41, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x9C, 0x8E, 0x8F, 0x28, 0x40, 0x96, 0xB9, 0xA3,
    0xFF
};

/* Graphics Controller: indices 0–8 */
static const u8 gc_regs[] = {
    0x00, 0x00, 0x00, 0x00, 0x00, 0x40, 0x05, 0x0F, 0xFF
};

/* Attribute Controller: indices 0–20 */
static const u8 ac_regs[] = {
    0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
    0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
    0x41, 0x00, 0x0F, 0x00, 0x00
};

/* ── Mode switch ──────────────────────────────────────────────────────────── */

void vga_init(void) {
    int i;

    /* 1. Miscellaneous output */
    port_byte_out(VGA_MISC_WRITE, misc_reg);

    /* 2. Sequencer */
    for (i = 0; i < 5; i++) {
        port_byte_out(VGA_SEQ_INDEX, (u8)i);
        port_byte_out(VGA_SEQ_DATA,  seq_regs[i]);
    }

    /* 3. Unlock CRTC registers 0–7 (clear protect bit in CR11) */
    port_byte_out(VGA_CRTC_INDEX, 0x11);
    port_byte_out(VGA_CRTC_DATA, port_byte_in(VGA_CRTC_DATA) & 0x7F);

    /* 4. CRT Controller */
    for (i = 0; i < 25; i++) {
        port_byte_out(VGA_CRTC_INDEX, (u8)i);
        port_byte_out(VGA_CRTC_DATA,  crtc_regs[i]);
    }

    /* 5. Graphics Controller */
    for (i = 0; i < 9; i++) {
        port_byte_out(VGA_GC_INDEX, (u8)i);
        port_byte_out(VGA_GC_DATA,  gc_regs[i]);
    }

    /* 6. Attribute Controller — reset flip-flop, then write index+data pairs */
    port_byte_in(VGA_INSTAT_READ);
    for (i = 0; i < 21; i++) {
        port_byte_out(VGA_AC_INDEX, (u8)i);
        port_byte_out(VGA_AC_INDEX, ac_regs[i]);
    }
    /* Enable video output (set PAS bit) */
    port_byte_out(VGA_AC_INDEX, 0x20);

    /* 7. Load a simple 16-colour VGA palette into DAC entries 0–15 */
    static const u8 pal16[16][3] = {
        {  0,  0,  0 }, {  0,  0, 42 }, {  0, 42,  0 }, {  0, 42, 42 },
        { 42,  0,  0 }, { 42,  0, 42 }, { 42, 21,  0 }, { 42, 42, 42 },
        { 21, 21, 21 }, { 21, 21, 63 }, { 21, 63, 21 }, { 21, 63, 63 },
        { 63, 21, 21 }, { 63, 21, 63 }, { 63, 63, 21 }, { 63, 63, 63 }
    };
    for (i = 0; i < 16; i++)
        vga_set_palette((u8)i, pal16[i][0], pal16[i][1], pal16[i][2]);
}

/* ── DAC palette ──────────────────────────────────────────────────────────── */

void vga_set_palette(u8 n, u8 r, u8 g, u8 b) {
    port_byte_out(VGA_DAC_WRITE, n);
    port_byte_out(VGA_DAC_DATA,  r);
    port_byte_out(VGA_DAC_DATA,  g);
    port_byte_out(VGA_DAC_DATA,  b);
}

/* ── Pixel primitives ─────────────────────────────────────────────────────── */

void vga_put_pixel(int x, int y, u8 colour) {
    VGA_FB[y * VGA_WIDTH + x] = colour;
}

u8 vga_get_pixel(int x, int y) {
    return VGA_FB[y * VGA_WIDTH + x];
}

void vga_clear(u8 colour) {
    int i;
    for (i = 0; i < VGA_WIDTH * VGA_HEIGHT; i++)
        VGA_FB[i] = colour;
}

void vga_hline(int x, int y, int len, u8 colour) {
    int i;
    volatile u8 *p = VGA_FB + y * VGA_WIDTH + x;
    for (i = 0; i < len; i++) p[i] = colour;
}

void vga_vline(int x, int y, int len, u8 colour) {
    int i;
    for (i = 0; i < len; i++)
        VGA_FB[(y + i) * VGA_WIDTH + x] = colour;
}

void vga_fill_rect(int x, int y, int w, int h, u8 colour) {
    int row;
    /* Clip */
    if (x < 0) { w += x; x = 0; }
    if (y < 0) { h += y; y = 0; }
    if (x + w > VGA_WIDTH)  w = VGA_WIDTH  - x;
    if (y + h > VGA_HEIGHT) h = VGA_HEIGHT - y;
    if (w <= 0 || h <= 0) return;

    for (row = 0; row < h; row++)
        vga_hline(x, y + row, w, colour);
}

void vga_draw_rect(int x, int y, int w, int h, u8 colour) {
    vga_hline(x,         y,         w, colour);
    vga_hline(x,         y + h - 1, w, colour);
    vga_vline(x,         y,         h, colour);
    vga_vline(x + w - 1, y,         h, colour);
}

