void vga_draw_char(int x, int y, char c, u8 fg, u8 bg) {
    int row, col;
    const u8 *glyph;
    if ((unsigned char)c < 0x20 || (unsigned char)c > 0x7E)
        glyph = font8x8[0];   /* space for unsupported chars */
    else
        glyph = font8x8[(unsigned char)c - 0x20];

    for (row = 0; row < 8; row++) {
        u8 bits = glyph[row];
        for (col = 0; col < 8; col++) {
            u8 colour = (bits & (1 << col)) ? fg : bg;
            vga_put_pixel(x + col, y + row, colour);
        }
    }
}

void vga_draw_string(int x, int y, const char *s, u8 fg, u8 bg) {
    int cx = x;
    while (*s) {
        if (cx + 8 > VGA_WIDTH) { cx = 0; y += 8; }
        vga_draw_char(cx, y, *s, fg, bg);
        cx += 8;
        s++;
    }
}
